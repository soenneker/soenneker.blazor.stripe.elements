namespace Soenneker.Blazor.Stripe.Elements.Configuration.Appearance;

/// <summary>
/// Style properties for input elements.
/// </summary>
public class StripeInputRule
{
    /// <summary>Background color of the input field.</summary>
    public string? BackgroundColor { get; set; }

    /// <summary>Border color of the input.</summary>
    public string? BorderColor { get; set; }

    /// <summary>Text color inside the input.</summary>
    public string? Color { get; set; }

    /// <summary>Box shadow applied on focus.</summary>
    public string? BoxShadow { get; set; }
}


